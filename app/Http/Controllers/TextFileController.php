    private function getValidAccountType($old)
    {
        if ($old ==  'Accounts Payable') {
            return 'Current Liability';
        } else if ($old == 'Accounts Receivable' || $old == 'Asset') {
            return 'Current Asset';
        } else if ($old == 'Asset') {
            return 'Current Asset';
        } else if ($old == 'Asset') {
            return 'Current Asset';
        } else if ($old == 'Cost of Sales') {
            return 'Direct Costs';
        } else if ($old == 'Credit Card') {
            return 'Current Liability';
        } else if ($old == 'Income') {
            return 'Sales';
        } else if ($old == 'Liability') {
            return 'Current Liability';
        } else if ($old == 'Long Term Liability') {
            return 'Non-current Liability';
        } else if ($old == 'Other Asset') {
            return 'Prepayment';
        } else if ($old == 'Other Current Asset') {
            return 'Inventory';
        } else if ($old == 'Other Current Liability') {
            return 'Current Liability';
        } else if ($old == 'Other Expense') {
            return 'Expense';
        } else if ($old == 'Other Liability') {
            return 'Current Liability';
        } else {
            return $old;
        }
    }
